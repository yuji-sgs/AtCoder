"""
H 行 W 列のグリッドがあり、はじめすべてのマスが白で塗られています。
グリッドの上から i 行目、左から j 列目のマスを (i,j) と表記します。
このグリッドはトーラス状であるとみなします。すなわち、各 1≤i≤H に対して (i,W) の右に (i,1) があり、各 1≤j≤W に対して (H,j) の下に (1,j) があるとします。
高橋君が (1,1) にいて上を向いています。高橋君が以下の操作を N 回繰り返した後のグリッドの各マスがどの色で塗られているか出力してください。
- 現在いるマスが白で塗られている場合は、現在いるマスを黒に塗り替え、時計回りに 90 ∘回転し、向いている方向に 1 マス進む。そうでない場合は、現在いるマスを白に塗り替え、反時計回りに 90∘回転し、向いている方向に 1 マス進む。
""" 
H, W, N = map(int, input().split())

# グリッドの初期化
grid = [['.' for _ in range(W)] for _ in range(H)]

# 高橋君の初期位置と向き（0: 上, 1: 右, 2: 下, 3: 左）
x, y, direction = 0, 0, 0

# 向きに応じた移動（上, 右, 下, 左）
dx = [-1, 0, 1, 0]
dy = [0, 1, 0, -1]

for _ in range(N):
    if grid[x][y] == '.':
        # マスを黒に塗り替え、時計回りに90度回転
        grid[x][y] = '#'
        direction = (direction + 1) % 4
    else:
        # マスを白に塗り替え、反時計回りに90度回転
        grid[x][y] = '.'
        direction = (direction - 1) % 4

    # 新しい位置に移動
    x = (x + dx[direction]) % H
    y = (y + dy[direction]) % W

# 結果の出力
for row in grid:
    print(''.join(row))
